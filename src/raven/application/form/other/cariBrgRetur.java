    private void loadData() {
    penjualanDAO dao = new penjualanDAO();
    List<modelBarang> list = dao.getTransaksiDetailByRef(ref);
    
    DefaultTableModel model = (DefaultTableModel) tabel.getModel();
    model.setRowCount(0); // Reset tabel
    
    for (modelBarang barang : list) {
    model.addRow(new Object[]{
        barang.getIdProduk(),
        barang.getNamaProduk(),
        (int) barang.getHargaProduk(), // Tampilkan sebagai integer
        barang.getStokProduk()
    });
}
}
