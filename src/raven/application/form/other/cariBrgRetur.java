    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        
        int selectedRow = tabel.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih barang terlebih dahulu!");
        return;
    }
    
    // Ambil data dari baris yang dipilih
    String idProduk = tabel.getValueAt(selectedRow, 0).toString();
    String namaProduk = tabel.getValueAt(selectedRow, 1).toString();
    int harga = Integer.parseInt(tabel.getValueAt(selectedRow, 2).toString());
    int qty = (int) Double.parseDouble(tabel.getValueAt(selectedRow, 3).toString());
    
    // Simpan ke objek modelBarang
    selectedBarang = new modelBarang();
    selectedBarang.setIdProduk(idProduk);
    selectedBarang.setNamaProduk(namaProduk);
    selectedBarang.setHargaProduk(harga);
    selectedBarang.setStokProduk(qty);
    
    // Tutup dialog
    dispose();
    }//GEN-LAST:event_simpanActionPerformed

